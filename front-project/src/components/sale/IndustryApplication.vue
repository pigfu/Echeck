        光绪二十九年（1903年）剪辫。课余喜读哲学与文艺之书，尤注意人性及国民性问题。 [24-25]
        光绪三十年（1904年），4月，于弘文学院结业。6月，祖父介孚公卒，年六十八。9月，入仙台医学专门学校（现日本东北大学），肄业，相识藤野严九郎。 [26]
        光绪三十二年（1906年），1月，课间观“日俄战争教育片”，深受刺激，决定弃医从文。6月，将学籍列入“东京独逸语协会”所设的德语学校。夏秋间，被骗回国与朱安结婚。旋即复赴日本，7月，从仙台回到东京，不再入学读书，专门从事文艺译著工作，此后几年通过不同方式学习了德语，俄语。 [23]  [27]
        光绪三十四年（1908年），从章太炎先生学习，为“光复会”会员，并与二弟作人译《域外小说集》，期间生活艰难，以校对书稿补贴生活。 [28]
        宣统元年（1909年），《域外小说集》二册出版。
